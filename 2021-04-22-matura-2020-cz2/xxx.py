"""
Moduł stworzony na zaliczenie informatyki na 2
"""

def xyz(n):
    """
    Funkcja xyz(n) zwraca liczbę trzy razy większą od n

    Przykład użycia:
    >>> xyz(10)
    30
    >>> xyz(1000)
    3000
    """
    return 3*n

def abc(n):
    """
    Funkcja abc zwraca liczbę o 4 mniejszą niż n
    """
    return n - 4
